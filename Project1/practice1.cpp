#include <iostream>
using namespace std;

int main() {
	int a = 10;
	double pie = 3.14;
	char ch = 'A';
	bool flag = true;

	cout << "정수: " << a << endl;
	cout << "실수: " << pie << endl;
	cout << "문자: " << ch << endl;
	cout << "논리: " << flag << endl;

	//산술 연산
	int x = 7, y = 3;
	cout << "x + y = " << x + y << endl;
	cout << "x % y = " << x % y << endl;

	return 0;
}